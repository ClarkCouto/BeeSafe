    private void preencherComboUsuarios() {
        model = new DefaultComboBoxModel();
        model.addElement("--Selecione--");
        List<Usuario> usuarios = bd.getUsuarios();
        for (Usuario b : usuarios) {
            model.addElement(b);
        }
        comboUsuarios.setModel(model);
    }
    
    private void preencherComboTiposUsuario() {
        model = new DefaultComboBoxModel();
        model.addElement("--Selecione--");
        List<TipoUsuario> tipos = bd.getTiposUsuario();
        for (TipoUsuario tu : tipos) {
            model.addElement(tu);
        }
        comboTiposUsuario.setModel(model);
    }

