    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        Usuario user;
        if(comboUsuarios.getSelectedIndex() != -1 && comboUsuarios.getSelectedIndex() != 0){
            user = (Usuario)comboUsuarios.getSelectedItem();
            user.setTipoUsuario((TipoUsuario)comboTiposUsuario.getSelectedItem());
            JOptionPane.showMessageDialog(this, "Usuario alterado com sucesso!", "Atenção!", JOptionPane.WARNING_MESSAGE);
        }
        else{
            JOptionPane.showMessageDialog(this, "E necessario selecionar um Usuario!", "Atenção!", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnEditarActionPerformed

